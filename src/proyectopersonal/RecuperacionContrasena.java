    private void usuarioTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_usuarioTxtActionPerformed
        // TODO add your handling code here:
        preguntaTxt.setText("");
        respuestaTxt.setVisible(false);
        verificarBtn.setVisible(false);
        metodos.buscarUsuario(usuarioTxt.getText(), preguntaTxt, respuestaTxt, verificarBtn);
        //se verifica si el usuario existe o no
        if(preguntaTxt.getText().equals("")){
            preguntaTxt.setVisible(true);
            preguntaTxt.setText("Usuario inv치lido");
        }else{
            usuarioTxt.setEditable(false);
            evt.setSource((char) KeyEvent.VK_CLEAR);
            respuestaTxt.requestFocus();
        }
    }//GEN-LAST:event_usuarioTxtActionPerformed

