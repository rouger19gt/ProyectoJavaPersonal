    private void cambiarBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cambiarBtnMousePressed
        // TODO add your handling code here:
        if(nuevaContra.getText().equals(confirmacionTxt.getText())){
            metodos.cambiarContrasena(usuarioTxt.getText(), nuevaContra.getText());
            setVisible(false);
            Ingreso ingreso = new Ingreso();
            ingreso.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Error: asegurese de escribir correctamente en la confirmaci칩n");
        }
    }//GEN-LAST:event_cambiarBtnMousePressed

