    private void crearBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearBtnMousePressed
        // TODO add your handling code here:
        //primero se valida que todos las cajas de textos estén llenas
        if(usuarioTxt.getText().length()>0 && correoTxt.getText().length()>0 && contrasenaTxt.getText().length()>0 || confirmarContrasenaTxt.getText().length()>0 &&
                preguntaTxt.getText().length()>0 && respuestaTxt.getText().length()>0){
            //si está llena entonces se hace la validación de contraseñas
            if(contrasenaTxt.getText().equals(confirmarContrasenaTxt.getText())){
                
                //Si la validación es correcta, entonces se valida si se inserta una imagen o no. 
                //Esto a través si el jlabel de la ruta está vacía.
                if(rutaLabel.getText().length()==0){
                       metodos.insertUsuarioSinFoto(usuarioTxt.getText(), correoTxt.getText(),  contrasenaTxt.getText(), 
                               preguntaTxt.getText(), respuestaTxt.getText(), 3);
                                irPanelSIguienteCliente();
                    
                    //al no estar vacía ya que al momento de elgir una imagen, se extrae la ruta en el label, en
                    //entonces hará lo siguiente
                }else{
                    //Se valida que tipo de usuario es, ya que en la BD Cliente =3, Empleado = 2, Administrador=3
                    //Esto en función de llaves foráneas
                       metodos.insertUsuario(usuarioTxt.getText(), correoTxt.getText(),  contrasenaTxt.getText(), 
                                preguntaTxt.getText(), respuestaTxt.getText(), 3, ruta);
                                irPanelSIguienteCliente();
                }
                //Validación incorrecta tira el siguiente mensaje:
            }else{
                JOptionPane.showMessageDialog(this, "Validación de contraseña incorrecta");
            }
            //Campos vacios tirará el siguiente mensaje
        }else{
            JOptionPane.showMessageDialog(this, "Debe de llenar todos los campos");
        }
    }//GEN-LAST:event_crearBtnMousePressed

    
    public void irPanelSIguienteCliente(){
        CrearCliente2 cliente = new CrearCliente2();
        cliente.setSize(880, 430);
        cliente.setLocation(0, 0);
        
        //tuve que hacer que el panel2 de la clase crudPersona fuera publica y static
        CrearUsuarioB.panel1.removeAll();
        CrearUsuarioB.panel1.add(cliente, BorderLayout.CENTER);
        CrearUsuarioB.panel1.revalidate();
        CrearUsuarioB.panel1.repaint();
        cliente.idTxt.setText(metodos2.obtenerUltimaIDCliente());
        cliente.idTxt.setEditable(false);
        cliente.usuarioTxt.setText(this.usuarioTxt.getText());//para asignar el usuario en la caja de texto del siguiente panel
                                                               //y así poder extraerlo en el método
        cliente.usuarioTxt.setEditable(false);//para que no se pueda editar la caja de texto
        
        metodos2.setObtenerusuario(usuarioTxt.getText());
        metodos2.setObtenertipo(3);
    }
    
