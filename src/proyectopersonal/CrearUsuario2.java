    private void buscarImagenBtnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarImagenBtnMousePressed
        // TODO add your handling code here:
        JFileChooser jfile = new JFileChooser();//manda a abrir un administrador de archivos
        int ap=jfile.showOpenDialog(this);
        if(ap == JFileChooser.APPROVE_OPTION){//si seleccionamos una imagene entonces hará lo siguiente:
            ruta = jfile.getSelectedFile().getAbsolutePath();//se obtiene la ruta de imagenen
            Image miimagen=new ImageIcon(ruta).getImage();
            ImageIcon miIcono = new ImageIcon(miimagen.getScaledInstance(imagenLabel.getWidth(), imagenLabel.getHeight(), 0));
            imagenLabel.setIcon(miIcono);
            rutaLabel.setText(ruta);
        }
    }//GEN-LAST:event_buscarImagenBtnMousePressed

